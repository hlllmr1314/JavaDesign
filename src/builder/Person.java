package builder;

/**
 * 
 * 产品定义类
 * 
 * @author huanglei
 *
 */
public class Person {
	private String head;
	private String body;
	private String foot;

	public String getBody() {
		return body;
	}

	public String getFoot() {
		return foot;
	}

	public String getHead() {
		return head;
	}

	public void setBody(String body) {
		this.body = body;
	}

	public void setFoot(String foot) {
		this.foot = foot;
	}

	public void setHead(String head) {
		this.head = head;
	}
}